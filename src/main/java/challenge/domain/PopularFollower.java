package challenge.domain;

public class PopularFollower {
	private int id;
	private String personName;
	private String popularFollowerName;
	public int getId() {
		return id;
	}
	public String getPersonName() {
		return personName;
	}
	public String getPopularFollowerName() {
		return popularFollowerName;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setPersonName(String personName) {
		this.personName = personName;
	}
	public void setPopularFollowerName(String popularFollowerName) {
		this.popularFollowerName = popularFollowerName;
	}
}
